
public class VariableDeclare1 {

	public static void main(String[] args) {
		int a; //변수선언 (타입 식별자)
		a = 10; //변수초기화(정수값 10 대입)
		System.out.println(a);
		a = 50;
		System.out.println(a);
		
		int b=20; //변수선언과 초기화
		System.out.println(b);

		int c,d,e;
		c=100;
		d=200;
		e=300;
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}

}
